package com.titstory.heowc.domain;

public class KeyClass {
	
	private String rm;
	private String primaryBtt;
	private String partnerBtt;
	private String visitingVeteran;
	private String count;
	public String getRm() {
		return rm;
	}
	public void setRm(String rm) {
		this.rm = rm;
	}
	public String getPrimaryBtt() {
		return primaryBtt;
	}
	public void setPrimaryBtt(String primaryBtt) {
		this.primaryBtt = primaryBtt;
	}
	public String getPartnerBtt() {
		return partnerBtt;
	}
	public void setPartnerBtt(String partnerBtt) {
		this.partnerBtt = partnerBtt;
	}
	public String getVisitingVeteran() {
		return visitingVeteran;
	}
	public void setVisitingVeteran(String visitingVeteran) {
		this.visitingVeteran = visitingVeteran;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	
	

}
